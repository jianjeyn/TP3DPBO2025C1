public class Manusia {
    private String nama;
    private String nohp;

    public Manusia(String nama, String nohp) {
        this.nama = nama;
        this.nohp = nohp;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getNoHP() {
        return nohp;
    }

    public void setNoHP(String nohp) {
        this.nohp = nohp;
    }
}
